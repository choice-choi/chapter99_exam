package org.joonzis.class03;

public class InfoClass {

	int empNo;
	String eName, job, hireDate;
	int sale, commission, deptNo;
	
	public InfoClass() {}
	public InfoClass(int empNo, String eName, String job, String hireDate, int sale, int commission, int deptNo) {
		this.empNo = empNo;
		this.eName = eName;
		this.job = job;
		this.hireDate = hireDate;
		this.sale = sale;
		this.commission = commission;
		this.deptNo = deptNo;
	}
	
	
	
}
